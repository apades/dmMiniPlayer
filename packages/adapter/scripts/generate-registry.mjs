import fs from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const adapterRoot = path.resolve(__dirname, '..')
const officialDir = path.resolve(adapterRoot, 'official')
const targetFile = path.resolve(officialDir, 'generated-registry.ts')

const ignored = new Set(['index.ts', 'generated-registry.ts'])

const files = (await fs.readdir(officialDir))
  .filter((file) => file.endsWith('.ts') && !ignored.has(file))
  .sort((a, b) => a.localeCompare(b))

const importLines = files.map((file, index) => {
  const importName = `adapterModule${index + 1}`
  const importPath = `./${file.replace(/\.ts$/, '')}`
  return {
    importLine: `import * as ${importName} from '${importPath}'`,
    importName,
    file,
  }
})

const mapBody = importLines
  .map(({ importName, file }) => {
    const key = `official/${file.replace(/\.ts$/, '')}`
    return `  '${key}': ${importName},`
  })
  .join('\n')

const output = `/* eslint-disable import/no-default-export */
/* eslint-disable import/no-duplicates */
/**
 * This file is auto-generated by \`packages/adapter/scripts/generate-registry.mjs\`.
 * Do not edit manually.
 */
${importLines.map((item) => item.importLine).join('\n')}

export const bundledSiteAdapterModules = {
${mapBody}
} as const
`

await fs.writeFile(targetFile, output, 'utf8')
console.log(
  `[adapter] generated ${path.relative(adapterRoot, targetFile)} with ${files.length} script(s).`,
)
